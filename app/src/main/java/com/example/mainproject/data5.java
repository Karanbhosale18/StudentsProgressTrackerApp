package com.example.mainproject;

public class data5 {
    String name,rollno,std;

    public data5(String name, String rollno, String std) {
        this.name = name;
        this.rollno = rollno;
        this.std = std;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getRollno() {
        return rollno;
    }

    public void setRollno(String rollno) {
        this.rollno = rollno;
    }

    public String getStd() {
        return std;
    }

    public void setStd(String std) {
        this.std = std;
    }
}
